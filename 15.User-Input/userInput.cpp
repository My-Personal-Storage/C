# include <stdio.h>

int main() {
    // Create a variable that will store the user's input
    int myNum ;

    // Ask the user to enter a number
    printf("Enter a number: ");

    // Get the user's input
    scanf("%d", &myNum);

    // Output the number the user entered
    printf("Your number is: %d\n", myNum);

    return 0;

}

