# include <stdio.h>

int main() {
    // 1. Addition
    int sum = 100 + 50; // 150

    // 2. Subtraction
    int difference = 100 - 50; // 50

    // 3. Multiplication
    int product = 100 * 50; // 5000

    // 4. Division
    int quotient = 100 / 50; // 2

    // 5. Modulus
    int remainder = 100 % 50; // 0

    // 6. Increment
    int x = 5;
    x++; // 6

    // 7. Decrement
    int y = 5;
    y--; // 4

    return 0;
}